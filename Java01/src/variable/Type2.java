package variable;

public class Type2 {
	public static void main(String[] args) {
		// 변수를 활용해서
		// 이름, 출생년도, 키
		
		/*
		 이름 : 000
		 출생년도 : 0000년
		 키 : 000.00cm
		 */
		
		String name = "백미르";
		int year = 2000;
		double height = 180.0;
		
		System.out.println("이름 : "+name);
		System.out.println("출생년도 : "+year+"년");
		System.out.println("키 : "+height+"cm");
	
	}
}
